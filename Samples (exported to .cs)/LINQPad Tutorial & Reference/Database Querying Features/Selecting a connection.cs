/* There are several ways to tell a query to use a connection.

!!The easiest is simply to drag the desired database from the Schema Explorer TreeView to the code editor!!

Here are the other ways:
	Drag any database object from the Schema Explorer to the editor
	Use the 'Connection' dropdown box above the editor
	Right-click the connection => Use in current query
	Press Ctrl+D - this sets the connection to whatever's currently selected in the Schema Explorer
	Use the hyperink right of the connection dropdown above the editor.

You can also press Ctrl+Shift+N on an existing query to create a new query with the same connection.

The shortcut to clear a query's connection is to press Ctrl+D (again, if necessary).

